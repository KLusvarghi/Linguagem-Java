public class subStrings {
    public static void main(String[] args) {
        String str = "Fatec Praia Grande";
        System.out.println(str.substring(6)); // mostra a classe de palavras a partir da posição que estiver dentro do
                                              // (), que é 6 no caso
        System.out.println(str.substring(0, 5));// mostra a classe de palavras a partir da primeira posição que estiver
                                                // dentro do () que é 0, até a outra posição, que no caso é 5
    }
}
