// Desenvolva um programa em Java para gerar a seguinte saída usando um laço for:
// 2
// 4
// 6
// 8
// 10
// Tchau!

public class Ex3 {
    public static void main(String[] args) throws Exception {

        for (int i = 2; i <= 10; i += 2) {
            System.out.println(i);
        }
        System.out.println("Tchau!");
    }
}
